private void loadDataToTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try (FileReader reader = new FileReader(filepath2)) {
            sched = (JSONArray) jsonParser.parse(reader);

            for (Object obj : sched) {
                JSONObject booking = (JSONObject) obj;
                model.addRow(new Object[]{
                        booking.get("time"),
                        booking.get("room"),
                        booking.get("month"),
                        booking.get("week"),
                        booking.get("day"),
                        booking.get("teacher")
                });
            }
        } catch (IOException | ParseException e) {
            e.printStackTrace();
        }
    }
