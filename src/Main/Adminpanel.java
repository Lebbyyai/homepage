    private void updtbttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updtbttnActionPerformed
       int row = jTable1.getSelectedRow();
        if (row != -1) {
            JSONObject updated = new JSONObject();
            updated.put("teacher", tcher.getText());
            updated.put("day", jCB2.getSelectedItem().toString());
            updated.put("month", jCB4.getSelectedItem().toString());
            updated.put("week", jCB6.getSelectedItem().toString());
            updated.put("time", jCB1.getSelectedItem().toString());
            updated.put("room", jCB5.getSelectedItem().toString());

            sched.set(row, updated);
            try (FileWriter writer = new FileWriter(filepath2)) {
                writer.write(sched.toJSONString());
            } catch (IOException e) {
                e.printStackTrace();
            }
            loadDataToTable();
            JOptionPane.showMessageDialog(this, "Booking updated!");
        }
    }//GEN-LAST:event_updtbttnActionPerformed

    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
      JSONObject booking = new JSONObject();
        booking.put("teacher", tcher.getText());
        booking.put("day", jCB2.getSelectedItem().toString());
        booking.put("month", jCB4.getSelectedItem().toString());
        booking.put("week", jCB6.getSelectedItem().toString());
        booking.put("time", jCB1.getSelectedItem().toString());
        booking.put("room", jCB5.getSelectedItem().toString());

        sched.add(booking);
        try (FileWriter writer = new FileWriter(filepath2)) {
            writer.write(sched.toJSONString());
            writer.flush();
        } catch (IOException e) {
            e.printStackTrace();
        }

        loadDataToTable();
        JOptionPane.showMessageDialog(this, "Booking added!");
    }//GEN-LAST:event_addbtnActionPerformed

    private void delbttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delbttnActionPerformed
        int row = jTable1.getSelectedRow();
        if (row != -1) {
            sched.remove(row);
            try (FileWriter writer = new FileWriter(filepath2)) {
                writer.write(sched.toJSONString());
            } catch (IOException ex) {
                Logger.getLogger(Facultypanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            loadDataToTable();
            JOptionPane.showMessageDialog(this, "Booking deleted!");
        }
    }//GEN-LAST:event_delbttnActionPerformed

